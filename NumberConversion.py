'''
Decimal to Binary Conversion, use bin function provided by python
'''

x = 25
y = bin(x)
print(y)

'''Decimal to Octal Conversion , use oct function provided by python'''

x = 25
y = oct(x)
print(y)

'''Decimal to hexadecimal Conversion , use hex function provided by python'''


x = 25
y = hex(x)
print(y)


